// src/services/ai/providers/MockProvider.ts
// ─────────────────────────────────────────────────────────────
// Mock AI provider for development, testing, and demos.
//
// Features:
//   • Zero API calls — works offline, costs nothing
//   • Section-aware responses (different text per generation step)
//   • Simulated streaming with configurable delay
//   • Deterministic — same input always produces same output
//   • Instant testConnection() — always succeeds
//
// Activate by setting AI Provider to "Mock — Demo Mode" in
// Configuration → AI Behavior.
// ─────────────────────────────────────────────────────────────

import type {
  IAIProvider,
  AIGenerationRequest,
  AIGenerationResult,
  AIStreamCallback,
  AIConnectionTest,
} from '../IAIProvider';

