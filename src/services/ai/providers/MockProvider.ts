// ── Word-by-word streaming delay ──────────────────────────────
// 25ms per word ≈ natural reading speed for demos.
// Set to 0 for unit tests to avoid test timeouts.

const DEFAULT_STREAM_DELAY_MS = 25;

const delay = (ms: number) => new Promise<void>(res => setTimeout(res, ms));

// Heuristic: match section IDs embedded in the prompt text

function resolveSectionId(prompt: string): string {
  const known = Object.keys(MOCK_NARRATIVES).filter(k => k !== '_default');
  const lp    = prompt.toLowerCase();
  return known.find(id => lp.includes(id.replace(/_/g, ' '))) ?? '_default';
}

// ── MockProvider ──────────────────────────────────────────────

export class MockProvider implements IAIProvider {
  readonly providerId   = 'mock';
  readonly modelId      = 'mock-v1';
  readonly displayName  = 'Mock — Demo Mode';

  private streamDelayMs: number;

  /**
   * @param streamDelayMs  Milliseconds between word emissions.
   *                       Default 25ms. Set to 0 for instant (unit tests).
   */
  constructor(streamDelayMs = DEFAULT_STREAM_DELAY_MS) {
    this.streamDelayMs = streamDelayMs;
  }

  private getMockText(request: AIGenerationRequest): string {
    const sectionId = resolveSectionId(request.prompt);
    return MOCK_NARRATIVES[sectionId] ?? MOCK_NARRATIVES._default;
  }

  // ── generate (non-streaming) ──────────────────────────────

  async generate(request: AIGenerationRequest): Promise<AIGenerationResult> {
    const startMs = Date.now();
    const text    = this.getMockText(request);

    // Small artificial pause so callers don't have to handle
    // synchronous-feeling async in edge cases
    await delay(this.streamDelayMs * 4);

    return {
      text,
      tokensGenerated: text.length,
      providerId:      this.providerId,
      modelId:         this.modelId,
      latencyMs:       Date.now() - startMs,
    };
  }

  // ── generateStream ────────────────────────────────────────

  async generateStream(
    request: AIGenerationRequest,
    onToken: AIStreamCallback,
  ): Promise<AIGenerationResult> {
    const startMs = Date.now();
    const text    = this.getMockText(request);
    const words   = text.split(' ');

    for (const word of words) {
      if (request.abortSignal?.aborted) break;
      onToken(word + ' ');
      if (this.streamDelayMs > 0) await delay(this.streamDelayMs);
    }

    return {
      text,
      tokensGenerated: text.length,
      providerId:      this.providerId,
      modelId:         this.modelId,
      latencyMs:       Date.now() - startMs,
    };
  }

  // ── testConnection ────────────────────────────────────────

  async testConnection(): Promise<AIConnectionTest> {
    return {
      ok:        true,
      message:   'Mock provider ready — no API connection required',
      latencyMs: 0,
    };
  }
}
